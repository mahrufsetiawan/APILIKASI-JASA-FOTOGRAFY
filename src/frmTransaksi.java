    public void getData( ){
     //menghapus isi table tblGaji
     model.getDataVector( ).removeAllElements( );
     model.fireTableDataChanged( );

     try{
           //membuat statemen pemanggilan data pada table tblGaji dari database
           Statement stat = (Statement) config.getKoneksi( ).createStatement( );
           String sql        = "Select * from transaksi";
           ResultSet res   = stat.executeQuery(sql);

           //penelusuran baris pada tabel tblGaji dari database
           while(res.next ()){
                Object[ ] obj = new Object[9];
                obj[0] = res.getString("id_transaksi");
                obj[1] = res.getString("id_fotografer");
                obj[2] = res.getString("nama_pemesan");
                obj[3] = res.getString("no_hp");
                obj[4] = res.getString("tanggal");
                obj[5] = res.getString("paket");
                obj[6] = res.getString("harga");
                obj[7] = res.getString("uang");
                obj[8] = res.getString("kembalian");

                model.addRow(obj);
            }
      }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage() );
      }
} 
    
    
