    public void tampil_combo(){
        try {
        Connection con = config.getKoneksi();
        Statement stt = con.createStatement();
        String sql = "select id_fotografer from fotografer order by id_fotografer asc";      // disini saya menampilkan NIM, anda dapat menampilkan
        ResultSet res = stt.executeQuery(sql);                                // yang anda ingin kan
        
        while(res.next()){
            Object[] ob = new Object[1];
            ob[0] = res.getString(1);
            
            combobox1.addItem((String) ob[0]);                                      // fungsi ini bertugas menampung isi dari database
        }
        res.close(); stt.close();
         
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
}
